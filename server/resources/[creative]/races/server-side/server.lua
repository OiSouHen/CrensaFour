-- FINISHRACE
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.finishRace(Selected,racePoints,vehName)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		if raceIlegal[user_id] ~= nil then
			vRP.generateItem(user_id,"dollars",math.random(1105,3505),true)
			vRP.generateItem(user_id,"racetrophy",1,true)
			TriggerEvent("blipsystem:serviceExit",source)
			raceIlegal[user_id] = nil
		end
		
		local identity = vRP.userIdentity(user_id)
		if identity then
        	local rows = vRP.query("races/racesWin",{ raceid = Selected, user_id = user_id })
        	if #rows > 0 then
        	    vRP.execute("races/updateRacesWin", { raceid = Selected, user_id = user_id, name = identity["name"].." "..identity["name2"], vehicle = vehName, points = racePoints, date = os.date("%d.%m.%Y")} )
			else
        	    vRP.execute("races/insertRacesWin", { raceid = Selected, user_id = user_id, name = identity["name"].." "..identity["name2"], vehicle = vehName, points = racePoints, date = os.date("%d.%m.%Y")} )
        	end
        end
	end
end
