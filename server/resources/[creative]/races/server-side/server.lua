-- REQUESTRANKING
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.requestRanking(raceid)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local identity = vRP.userIdentity(user_id)
		if identity then
			local resultRanking = vRP.query("races/showRacesWin",{ raceid = raceid })
			local resultTable = {}
			
			for k,v in pairs(resultRanking) do
				local name = identity["name"].." "..identity["name2"]
				local points = v.points
				table.insert(resultTable,{ k = k, name = v.name, vehicle = vehicleName(v.vehicle), points = points })
			end

			return resultTable
		end
	end
end
